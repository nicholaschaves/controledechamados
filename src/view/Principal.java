    private void cadastrar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_btnActionPerformed
        if(requerente_input.getText().isEmpty() || setor_input.getText().isEmpty() || reclamacao_input.getText().isEmpty() || conclusao_input.getText().isEmpty() || status_combo.getSelectedIndex() == 0 || comboAtendente.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Favor, preencher todos os campos!", "Ausência de informações", 1);
        } else {
        
            AcessoBanco.insertData(requerente_input.getText(), setor_input.getText(), reclamacao_input.getText(), conclusao_input.getText(), status_combo.getSelectedItem().toString(), data_atualizada.getText(), comboAtendente.getSelectedItem().toString());
        
            requerente_input.setText("");
            setor_input.setText("");
            reclamacao_input.setText("");
            conclusao_input.setText("");
            status_combo.setSelectedIndex(0);
            comboAtendente.setSelectedIndex(0);
        
        }
        
        
        
    }//GEN-LAST:event_cadastrar_btnActionPerformed


    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        
        TableModel modelo = jTable1.getModel();
        id_blocked.setText(modelo.getValueAt(selectedRow, 0).toString());
        requerente_update.setText(modelo.getValueAt(selectedRow, 1).toString());
        setor_update.setText(modelo.getValueAt(selectedRow, 2).toString());
        reclamacao_update.setText(modelo.getValueAt(selectedRow, 3).toString());
        conclusao_update.setText(modelo.getValueAt(selectedRow, 4).toString());
        String statusTemp = modelo.getValueAt(selectedRow, 5).toString();
        comboStatus_update.setSelectedItem(statusTemp);
        String data_temp = AcessoBanco.consultarData(Integer.parseInt(id_blocked.getText()));
        dataChamado_block.setText(data_temp);
        String nome_temp = AcessoBanco.consultarAtendente(Integer.parseInt(id_blocked.getText()));
        comboAtendente_block.setText(nome_temp);
    }//GEN-LAST:event_jTable1MouseClicked

